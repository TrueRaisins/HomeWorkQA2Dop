package Primitives;

public class Primitives {
  protected static void main (String[] args) {
    // Целочисленные типы
    byte TestByte = -128;
    short TestShort =-32768;
    int TestInt = -2147483648;
    long TestLong = -922372036854775808L;
    // Символы
    char TestChar = 'f';
    // Типы с плавающей точкой
    float TestFloat = 3.4028235E38F;
    double TestDouble = 1.7976931348623157E308;
    // Логический тип
    boolean TestBoolean = true;
  }
}
